    private void BuyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuyButtonActionPerformed

        String address = addressTF.getText();
        String phone = phoneTF.getText();
        int quantity = quantityBuy;
        Date date = dateChooser.getDate();
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String dateString = dateFormat.format(date.getTime());
//        System.out.print('\n'+quantity);
//        System.out.print("\n"+dateString);
//        System.out.print("\n"+ total);
//        System.out.print("\n"+ userId);
//        System.out.print("\n"+ phone);
//        System.out.print("\n"+ address);
        
        Connection connection = null;
        try {
            connection = ConnectionDB.getConnection();
            String message;
            MessageDialog messageDialog = new MessageDialog();
            String insertQuery = "INSERT INTO orders(address, phone, user_id, quantity, total_price, product_id, date, status) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
            try (PreparedStatement preparedStatement = connection.prepareStatement(insertQuery)) {
                preparedStatement.setString(1, address);
                preparedStatement.setString(2, phone);
                preparedStatement.setInt(3, userId);
                preparedStatement.setInt(4, quantity);
                preparedStatement.setInt(5, total);
                preparedStatement.setInt(6, data.getId());
                preparedStatement.setString(7, dateString);
                preparedStatement.setString(8, "Chờ Duyệt");
                
                int rowsAffected = preparedStatement.executeUpdate();
                if (rowsAffected == 1) {
                    message = "Đặt Hàng Thành Công";
                    UpdateQuantityProduct();
                    messageDialog.inforMessegeDiaLog(message, "Alert !!");
                    dispose();
                } else {
                    message = "Đặt Hàng Thất Bại Vui Lòng Kiếm Tra Lại Thông Tin!! ";
                    messageDialog.inforMessegeDiaLog(message, "Alert !!");
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_BuyButtonActionPerformed

