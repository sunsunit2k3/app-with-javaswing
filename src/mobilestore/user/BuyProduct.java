    private void quantitySpinnerStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_quantitySpinnerStateChanged
        quantityBuy = (int) quantitySpinner.getValue();
        MessageDialog messageDialog = new MessageDialog();
        if(quantityBuy>data.getQuantity()){
            messageDialog.inforMessegeDiaLog("Số lượng vượt mua vượt quá số lượng trong kho", "Alert !!");
        }else if(quantityBuy<0){
            messageDialog.inforMessegeDiaLog("Vui lòng chọn đúng số lượng cần mua", "Alert !!");
        }else{
        total = quantityBuy*(int)data.getPrice();
        totalLabel.setText(Integer.toString(total) + " VND");
        }
    }//GEN-LAST:event_quantitySpinnerStateChanged

